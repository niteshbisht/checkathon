package com.server.beans;

public class GrievPost {

	private String stateid;
	private String regid;
	private String depid;
	private String grievanceString;
	
	public String getStateid() {
		return stateid;
	}
	public void setStateid(String stateid) {
		this.stateid = stateid;
	}
	public String getRegid() {
		return regid;
	}
	public void setRegid(String regid) {
		this.regid = regid;
	}
	public String getDepid() {
		return depid;
	}
	public void setDepid(String depid) {
		this.depid = depid;
	}
	public String getGrievanceString() {
		return grievanceString;
	}
	public void setGrievanceString(String grievanceString) {
		this.grievanceString = grievanceString;
	}
	
}
